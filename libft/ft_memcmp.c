#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*f;
	unsigned char	*s;

	i = 0;
	f = (unsigned char *)s1;
	s = (unsigned char *)s2;
	while (i < n && f[i] == s[i])
		i++;
	if (i == n)
		return (0);
	return ((f[i]) - (s[i]));
}
